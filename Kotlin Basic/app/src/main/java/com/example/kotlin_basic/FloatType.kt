package com.example.kotlin_basic
fun main (){
    // Var Data type is muteable that mean we can change the value of var data type after assigning
    var  circleRatio = 3.14
    println("Circle Ratio : "+circleRatio)

    // Modifying Double to Float
    var circleRatio2 = 3.14f
    println(circleRatio2)

    var circleRatio3 = 3.1415926535f
    println(circleRatio3)

    var circleRatio4 = 3.14159265358979328754
    println(circleRatio4)

}